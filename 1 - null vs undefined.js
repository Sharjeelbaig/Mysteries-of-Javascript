/* Null vs undefined */

let a; // undefined
let b = null; // null

console.log("a:", a); // undefined
console.log("b:", b); // null

/*
Null is a special value that
represents the absence of a value or a non-existent object.
It is not a type, but rather a value that can be assigned to
a variable of any reference type. while undefined is a type itself.
*/
